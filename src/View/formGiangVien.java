    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if("".equals(txtMaGV.getText()) || "".equals(txtHocVi.getText()) || "".equals(txtHoTen.getText()))
        {
            JOptionPane.showMessageDialog(null, "Yêu cầu nhập đẩy đủ thông tin!", "Thông báo!", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            GiangVienDAO gvDAO = new GiangVienDAO();
            GiangVien gv = new GiangVien();
            gv.setIdGiangVien(txtMaGV.getText());
            gv.setTenGiangVien(txtHoTen.getText());
            gv.setHocVi(txtHocVi.getText());
            
            if(gvDAO.ThemGiangVien(gv)==-1)
            {
                JOptionPane.showMessageDialog(null, "Trùng mã giảng viên!", "Thông báo!", JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Thêm thành công giảng viên " + txtHoTen.getText());
            }
            loadData();
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
       
        int row = tableGiangVien.getRowCount();
        
        if ("".equals(txtMaGV.getText())) 
        {
            JOptionPane.showMessageDialog(null, "Yêu cầu nhập mã giảng viên để xóa!", "Thông báo!", JOptionPane.WARNING_MESSAGE);
        } 
        
        else 
        {
            GiangVienDAO gvDAO = new GiangVienDAO();
            GiangVien gv = new GiangVien();
            gv.setIdGiangVien(txtMaGV.getText());

            if (gvDAO.XoaGiangVien(gv) == -1) 
            {
                JOptionPane.showMessageDialog(null, "Xóa thất bại!", "Thông báo!", JOptionPane.ERROR_MESSAGE);
            } 
            else 
            {                
                loadData();
                int temp = tableGiangVien.getRowCount();
                if(row>temp)                    
                    JOptionPane.showMessageDialog(null, "Xóa thành công");
                else
                    JOptionPane.showMessageDialog(null, "Nhập sai mã!", "Thông báo!", JOptionPane.ERROR_MESSAGE);
            }
        }        
    }//GEN-LAST:event_btnXoaActionPerformed


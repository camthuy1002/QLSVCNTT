    public void loadData()
    {
        GiangVienDAO gvDAO = new GiangVienDAO();
        ArrayList<GiangVien> dsgv = gvDAO.DanhSachGiangVien();
        
        DefaultTableModel tableGV = (DefaultTableModel) this.tableGiangVien.getModel();
        tableGV.setRowCount(0);
        int stt = 0;
        for(GiangVien gv : dsgv)
        {
            stt++;
            Object[] row = new Object[]{ String.valueOf(stt) ,gv.getIdGiangVien(), gv.getTenGiangVien(), gv.getHocVi()};
            tableGV.addRow(row);
        }
        int sl = tableGiangVien.getRowCount();
        txtTongSoGV.setText(String.valueOf(sl));
    }
    
